#include <stdio.h>

void	ft_swap(int *a, int *b)
{
	int	temp;

	temp = *a;
	*a = *b;
	*b = temp;
}

int	main(void)
{
	int	a;
	int	b;

	a = 32;
	b = 16;
	printf("%i, %i", a, b);
	ft_swap(&a, &b);
	printf("%i, %i", a, b);
	return (1);
}
