char	*ft_strncpy(char *dest, char *src, unsigned int n)
{	
	char	*p;

	p = dest;
	while (*src != '\0' && n-- > 0)
	{
		*dest++ = *src++;
	}
	if (*src == '\0')
		*dest = '\0';
	return (p);
}
