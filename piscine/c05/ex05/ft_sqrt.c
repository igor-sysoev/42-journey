int	ft_sqrt(int nb)
{
	float	i;

	i = 2.00;
	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	while (nb / i != i && i < nb)
		i++;
	if (i == nb)
		return (0);
	return (i);
}
