char	*ft_strncat(char *dest, char *src, unsigned int n)
{
	char	*p;

	p = dest;
	while (*dest != '\0')
		dest++;
	while (*src != '\0' && n-- > 0)
		*dest++ = *src++;
	*dest = '\0';
	return (p);
}
