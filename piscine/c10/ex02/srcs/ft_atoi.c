#include "ft.h"

int	ft_atoi(char *str)
{
	int	num;
	int	is_neg;

	is_neg = 1;
	num = 0;
	if (*str == '-')
	{
		is_neg = -1;
		str++;
	}
	while (*str != '\0')
	{
		num = num * 10 + (*str - '0');
		str++;
	}
	return (num * is_neg);
}
