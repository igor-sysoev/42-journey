#include <stdio.h>

int	ft_any(char **tab, int (*f)(char *))
{
	int	res;

	res = 0;
	if (f == NULL)
		return (0);
	while (*tab != NULL)
	{
		res = f(*tab++);
		if (res != 0)
			return (1);
	}
	return (0);
}
