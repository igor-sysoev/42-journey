int	ft_isdigit(char c)
{
	if (c >= '0' && c <= '9')
		return (1);
	return (0);
}

int	ft_str_is_numeric(char *s)
{
	while (*s != '\0')
	{
		if (!ft_isdigit(*s++))
			return (0);
	}
	return (1);
}
