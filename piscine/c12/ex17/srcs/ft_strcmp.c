#include <ft.h>

int	ft_strcmp(char *str1, char *str2)
{	
	int	x;
	
	x = 0;
	while (str1[x] == str2[x] && str1[x] != '\0' && str2[x] != '\0')
		x++;
	return (str1[x] - str2[x]);
}

